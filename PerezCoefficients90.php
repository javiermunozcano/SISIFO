function PerezCoefficients90($epsilon) {
	
//Coefficients of Perez model
//Perez et al., Solar energy 44, 1990
	if ($epsilon <= 1.065) 
	{
	$k31=-0.008;
	$k32= 0.588;
	$k33=-0.062;
	$k41=-0.060;
	$k42= 0.072;
	$k43=-0.022;
	}elseif ($epsilon <= 1.23)
		{
		$k31= 0.130;
		$k32= 0.683;
		$k33=-0.151;
		$k41=-0.019;
		$k42= 0.066;
		$k43=-0.029;
		}elseif ($epsilon <= 1.5)
			{
			$k31= 0.330;
			$k32= 0.487;
			$k33=-0.221;
			$k41= 0.055;
			$k42=-0.064;
			$k43=-0.026;
			}elseif ($epsilon <= 1.95)
				{
				$k31= 0.568;
				$k32= 0.187;
				$k33=-0.295;
				$k41= 0.109;
				$k42=-0.152;
				$k43=-0.014;
				}elseif ($epsilon <= 2.8)
					{
					$k31= 0.873;
					$k32=-0.392;
					$k33=-0.362;
					$k41= 0.226;
					$k42=-0.462;
					$k43= 0.001;
					}elseif ($epsilon <= 4.5)
						{
						$k31= 1.132;
						$k32=-1.237;
						$k33=-0.412;
						$k41= 0.288;
						$k42=-0.823;
						$k43= 0.056;
						}elseif ($epsilon <= 6.2)
							{
							$k31= 1.060;
							$k32=-1.600;
							$k33=-0.359;
							$k41= 0.264;
							$k42=-1.127;
							$k43= 0.131;
							}else 
								{
								$k31= 0.678;
								$k32=-0.327;
								$k33=-0.250;
								$k41= 0.156;
								$k42=-1.377;
								$k43= 0.251;
								}
$PC=array(
		'k31' => $k31,
		'k32' => $k32,
		'k33' => $k33,
		'k41' => $k41,
		'k42' => $k42,
		'k43' => $k43);
								
return $PC;
}

